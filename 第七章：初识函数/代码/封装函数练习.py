# 1、小梅数她家的鸡与兔，数头有16个，数脚有44只。问：小梅家的鸡与兔各有多少只？
for ji in range(0,17,1):
    tu = 16-ji
    if 4*tu+2*ji == 44:
        print("鸡的数量：", ji, "兔子数量：", tu)

# 2、100个和尚140个馍，大和尚1人分3个馍，小和尚1人分1个馍。问：大、小和尚各有多少人？
for da in range(0,101,1):
    xiao = 100-da
    if da*3 + xiao*1 == 140:
        print(da, xiao)

