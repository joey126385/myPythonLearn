# 假设初始值是 x ,终点值是y ,步长是 z
# 参数放的是函数在使用过程中会发生改变的值
def ct(x,y,z):
    c = 0
    for i in range(x,y,z):
        c = c+i
    print(f"当前累加结果：{c}")


# 调用函数
# 1、求100中奇数和
ct(1,100,2)

# 2、求100中偶数和
ct(2,101,2)


