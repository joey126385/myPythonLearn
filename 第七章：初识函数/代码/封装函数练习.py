# 这几个数据，是需要我们额外指定的的，其他的内容都可以重复       总数,x,y,总数2
def ji_tu(总数,x,y,总数2):
    for 变量1 in range(0,总数+1,1):
        变量2 = 总数-变量1
        if 变量1*x + 变量2*y == 总数2:
            print(变量1,变量2)
# 调用一下函数
# 1、小梅数她家的鸡与兔，数头有16个，数脚有44只。问：小梅家的鸡与兔各有多少只？
