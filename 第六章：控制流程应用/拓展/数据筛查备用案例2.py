"""
题目：
编写一个Python程序，给定一个包含字典的列表，每个字典代表一个商品的信息，包含"名称"、"价格"和"数量"三个键。
使用for循环遍历这个列表，并计算所有商品的总价格。同时，如果某个商品的价格大于100，则在计算总价格时将价格减少10%。

示例列表：
products = [
    {"名称": "商品A", "价格": 80, "数量": 2},
    {"名称": "商品B", "价格": 120, "数量": 1},
    {"名称": "商品C", "价格": 50, "数量": 3},
    {"名称": "商品D", "价格": 150, "数量": 1}
]

预期输出：
总价格为：337元

要求：
使用for循环遍历列表中的每个字典。
对于每个字典，计算单个商品的总价格（价格乘以数量）。
如果商品的价格大于100，则减少10%后再计算总价格。
累加所有商品的总价格得到总价格。
打印出最终的总价格。

"""

products = [
    {"名称": "商品A", "价格": 80, "数量": 2},
    {"名称": "商品B", "价格": 120, "数量": 1},
    {"名称": "商品C", "价格": 50, "数量": 3},
    {"名称": "商品D", "价格": 150, "数量": 1}
]

total_price = 0

for product in products:
    price = product["价格"]
    quantity = product["数量"]

    # 如果价格大于100，则减少10%
    if price > 100:
        price *= 0.9
        # 计算单个商品的总价格并累加到总价格中
    total_price += price * quantity

# 打印总价格
print("总价格为：{}元".format(total_price))