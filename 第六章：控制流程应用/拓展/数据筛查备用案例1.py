"""
题目：
编写一个Python程序，给定一个包含字典的列表，每个字典代表一个学生的信息，包含"姓名"、"年龄"和"成绩"三个键。
使用for循环遍历这个列表，并找出年龄大于20岁的学生的姓名和成绩，将它们打印出来。如果某个学生的年龄不大于20岁，则忽略该学生的信息。

示例列表：
students = [
    {"姓名": "小明", "年龄": 22, "成绩": 90},
    {"姓名": "小红", "年龄": 19, "成绩": 85},
    {"姓名": "小刚", "年龄": 23, "成绩": 92},
    {"姓名": "小丽", "年龄": 21, "成绩": 88}
]

预期输出：
小明, 年龄: 22, 成绩: 90
小刚, 年龄: 23, 成绩: 92
小丽, 年龄: 21, 成绩: 88

要求：
使用for循环遍历列表中的每个字典。
对于每个字典，检查"年龄"键对应的值是否大于20。
如果年龄大于20，则打印出该学生的"姓名"、"年龄"和"成绩"。如果年龄不大于20，则不执行任何操作。

"""

students = [
    {"姓名": "小明", "年龄": 22, "成绩": 90},
    {"姓名": "小红", "年龄": 19, "成绩": 85},
    {"姓名": "小刚", "年龄": 23, "成绩": 92},
    {"姓名": "小丽", "年龄": 21, "成绩": 88}
]
for student in students:
    age = student["年龄"]
    if age > 20:
        name = student["姓名"]
        score = student["成绩"]
        print(f"{name}, 年龄: {age}, 成绩: {score}")